function buildPhaserPrompt(ctx) {
  return `You are an expert Phaser.js 3 game developer.

Generate a COMPLETE, WORKING Phaser.js 3 platformer game based on the GDD provided.

MANDATORY REQUIREMENTS:
1. Use Phaser.Scene classes (NOT function-based scenes)
2. Create PreloadScene, GameScene, and GameOverScene classes
3. Player must have: left/right movement, jumping, gravity
4. Include at least 2 enemy instances with patrol AI
5. Include platforms using staticGroup with colored rectangles
6. Implement the FIRST core mechanic from the GDD
7. Player uses keyboard: arrow keys for movement, SHIFT or E for special mechanic
8. Camera must follow the player
9. Include health system (3 lives) and game over logic
10. Name ALL classes using the actual game characters from the GDD

CRITICAL - PLACEHOLDER GRAPHICS:
- NEVER use this.physics.add.sprite() with a texture key
- NEVER load images in preload()
- NEVER call .create() on a staticGroup with a texture key
- For player: this.add.rectangle() then this.physics.add.existing(rect)
- For static platform: this.add.rectangle() then this.physics.add.existing(rect, true)
- For enemy: this.add.rectangle() then this.physics.add.existing(rect)

Return ONLY valid JSON:
{
  "engine": "phaser",
  "files": [
    { "filename": "game.js", "description": "Complete Phaser.js 3 game", "content": "..." }
  ],
  "architecture_md": "..."
}`
}

